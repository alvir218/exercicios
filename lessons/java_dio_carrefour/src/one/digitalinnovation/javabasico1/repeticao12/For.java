package one.digitalinnovation.javabasico1.repeticao12;

public class For {
    public static void main(String[] args) {

        for(int i = 0; i <= 10; i = i + 1){
            System.out.println("iteracao: " + i);
        }

        for(int x = 0; x <= 5; x++){
            System.out.println("iteracao: " + x);
        }
    }
}
