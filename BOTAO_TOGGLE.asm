	RETORNARTOGGLEK3

	BTFSC CONTADOR0,2
		;verifica o bit posicional 2 DO CONTADOR0
		;e pula se for 0
	GOTO ACENDERLED2
	
	RETORNARACENDERLED2



	BTFSS PORTB,3
		;verifica o bit posicional 0 DO PORTB
		;e pula se for 1
		;LINHA 3 POR PADRAO
		;SE O BOTAO K1 ESTIVER APERTADO
