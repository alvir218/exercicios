	RETORNARTOGGLEK4

	BTFSC CONTADOR0,3
		;verifica o bit posicional 0 DO CONTADOR0
		;e pula se for 0
	GOTO ACENDERLED3
	
	RETORNARACENDERLED3





GOTO LOOP



